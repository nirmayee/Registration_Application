    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        this.disp_image = null;
        JFileChooser chooser = new JFileChooser();
        
        FileNameExtensionFilter fe = new FileNameExtensionFilter("IMAGES","png","jpg","jpeg");
        chooser.addChoosableFileFilter(fe);
        
        int OpenDialogue = chooser.showSaveDialog(null);
        
        if(OpenDialogue == JFileChooser.APPROVE_OPTION){
            File ImageFile = chooser.getSelectedFile();
            
            try{
                this.disp_image = ImageIO.read(ImageFile);
                ImageIcon img = new ImageIcon(disp_image);
                Image Img = img.getImage().getScaledInstance(jLabelImg.getWidth(),jLabelImg.getHeight(), Image.SCALE_SMOOTH);
                jLabelImg.setIcon(new ImageIcon(Img));
            }catch (IOException ioe){
                JOptionPane.showMessageDialog(this, "Image is mandatory!");
            }
        }
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed


