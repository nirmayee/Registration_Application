    private void savemethod(){
        product.setName(name.getText());
        
        product.setGeographicdata(txtgeographicdata.getText());
        
        product.setDate(jDateChooser1.getDate());
        
          //telephone
    long tel = Integer.parseInt(txttelephone.getText());
product.setTelephone(tel);

 //fax 
     long f = Integer.parseInt(txtfax.getText());
      product.setFax(f);
      
    //email
      product.setEmail(txtemail.getText()); 
      
       //SSN
     long s = Integer.parseInt(txtssn.getText());
    product.setSsn(s);  
    
    long MedNum = Integer.parseInt(txtmed.getText());
     product.setMedicalnumber(MedNum);
     
      long Health = Integer.parseInt(txthealthplannumber.getText());
  product.setHealthplannumber(Health);
  
  product.setBankaccount(txtbankaccount.getText());
  
   product.setLicense(txtlicense.getText());
   
    product.setVehicleidentifier(txtvehicleidentifier.getText());
    
    product.setDeviceidentifiers(txtdeviceidentifiers.getText());
    
    product.setLinkedIn(txtlinkedIn.getText());
    
   
    product.setIpaddress(txtipaddress.getText());
     
     //Uniquecode
      product.setUniquecode(txtuniquecode.getText());
      
      product.setFilename(jLabelImg.getIcon());

        
        JOptionPane.showMessageDialog(this,"Product Information Saved!");
    }
